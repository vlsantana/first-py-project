#### VARIABLES ####
# type of primitive variables in python:
# string --> one or more characters: "vania"
# integer --> decimal numbers: [0-9]
# float --> non decimal numbers: [0.1-9.9]
# boolean --> True or False/ 1 or 0
 
# how to set variables: 
#

name = "Ritchy"
print(name)
print(type(name))

age = 10
print(age)
print(type(age))

age = 0.20
print(age)
print(type(age))

is_male = True 
print(is_male)
print(type(is_male))

is_female = False
print(is_female)
print(type(is_female))

is_open = "1"
print(is_open)
print(type(is_open))
